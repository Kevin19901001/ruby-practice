# 'if' control structure:
weight = 105

if weight > 100
  puts "You are too fat!"
else
  puts "Very healthy!"
end


# 'while' control structure:
square = 2

while square < 666
  p square
  square = square*square
end


# Use statement modifier, the statement will be more succinct:
square = 3
square = square*square while square < 666
p square
